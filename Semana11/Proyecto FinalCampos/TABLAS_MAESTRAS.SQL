CREATE TABLE CONTADOR
( 
	IDCONTADOR           integer  NOT NULL ,
	TABLA                varchar(100)  NOT NULL ,
	ULTIMO               integer  NOT NULL ,
	CONSTRAINT XPKCONTADOR PRIMARY KEY (IDCONTADOR ASC)
)
go



CREATE TABLE AREA
( 
	IDAREA               integer  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	CONSTRAINT XPKAREA PRIMARY KEY (IDAREA ASC)
)
go



CREATE TABLE CURSO
( 
	IDCURSO              integer  NOT NULL ,
	IDAREA               integer  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	CANTIDAD_HORAS       int  NOT NULL ,
	PRECIO               money  NOT NULL ,
	MODULO               varchar(100)  NOT NULL ,
	CONSTRAINT XPKCURSO PRIMARY KEY (IDCURSO ASC),
	CONSTRAINT R_23 FOREIGN KEY (IDAREA) REFERENCES AREA(IDAREA)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE ALUMNO
( 
	IDALUMNO             integer  NOT NULL ,
	APELLIDOS            varchar(50)  NOT NULL ,
	NOMBRE               varchar(50)  NOT NULL ,
	TELEFONO             varchar(20)  NULL ,
	CORREO               varchar(100)  NOT NULL ,
	DNI                  varchar(20)  NOT NULL ,
	CONSTRAINT XPKALUMNO PRIMARY KEY (IDALUMNO ASC)
)
go

